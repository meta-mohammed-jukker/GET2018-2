package counselling;
/**
 * 
 * Class for student
 *
 */
public class Student {
	private String name;
	private String preference;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPreference() {
		return preference;
	}

	public void setPreference(String preference) {
		this.preference = preference;
	}
}
